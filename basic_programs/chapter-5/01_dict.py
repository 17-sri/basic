marks = {"abd": 77,
         "msd": 7,
         "ro": 45,
         "score": [99, 71, 26, 22]}
print(type(marks)) # returns 'dict'
print(marks)
print(marks["score"]) # returns [99, 71, 26, 22]
print(marks["ro"]) # returns 45 ; we can access the values using corresponding keys


